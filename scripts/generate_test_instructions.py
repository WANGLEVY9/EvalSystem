def generate_instruction_file(data: dict) -> str:
    """Generate markdown instruction content from structured data."""
    lines = []
    # Role
    lines.append(f"# Role\n{data['role']}\n")
    # Task
    lines.append(f"# Task\n{data['task']}\n")
    # Opening Line
    lines.append(f"# Opening Line\n{data['opening']}\n")
    # Call Flow
    lines.append("# Call Flow")
    for i, (title, desc) in enumerate(data['flow'], 1):
        lines.append(f"{i}. **{title}**：{desc}")
    lines.append("")
    # Knowledge Points
    lines.append("# Knowledge Points (FAQ)")
    for kp in data['knowledge']:
        lines.append(f"- **{kp['q']}** {kp['a']}")
    lines.append("")
    # Constraints
    lines.append("# Constraints")
    for c in data['constraints']:
        lines.append(f"- {c}")
    lines.append("")

    return "\n".join(lines)


def main():
    count = 0
    for instr in INSTRUCTIONS:
        filename = f"{instr['name']}.md"
        filepath = TEST_DIR / filename
        content = generate_instruction_file(instr)
        filepath.write_text(content, encoding="utf-8")
        count += 1
        print(f"  [{count:2d}] {filename} ({len(content)} bytes)")

    print(f"\n✅ 已生成 {count} 个模拟指令文件到 {TEST_DIR}")
    print(f"📁 路径: {TEST_DIR}")


if __name__ == "__main__":
    main()
